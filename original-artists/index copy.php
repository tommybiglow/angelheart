					<h3>SANFORD SYLVAN</h3>
					<p>From Schubert's Die Schöne Müllerin and the Passions of J.S. Bach to the operas of John Adams, American baritone Sanford Sylvan displays a remarkable range of vocal expression and communicative power. On the concert original_artists and in recordings, his radiantly pure, lyric tone, clarity of diction and profound understanding of both words and music speak directly and intimately to his audience.</p>
					<p>Deeply committed to the art of the vocal recital, Mr. Sylvan and his long-time collaborator, pianist David Breitman, have performed extensively throughout the United States and Europe, in major venues in London, New York, Washington, Boston, Philadelphia and San Francisco. Theirrecitals and recordings have earned exceptional praise from critics and audiences, including five Grammy nominations.</p>
					<p>In the realm of opera, Mr. Sylvan is an acclaimed Mozartean. His portrayals of Figaro in Le Nozze di Figaro and Don Alfonso in Cosi fan tutte have been seen internationally, including PBS "Great Performances." He has been much acclaimed for the role of Leporello in Don Giovanni, which he sang for his Glyndebourne Festival debut and with New York City Opera, where he has since become a regular performer in such operas as The Magic Flute, Ariodante, The Rape of Lucretia and Handel’s Semele. Sanford Sylvan is closely associated with the productions of renowned directors: Peter Sellars in works of John Adams, Mozart and Stravinsky; Robert Wilson in Virgil Thomson's Four Saints in Three Acts, as well as Sir Peter Hall and Andrei Serban. He has developed longstanding relationships with major composers who have written for him: John Adams' Nixon In China (Chou En-Lai), the title role of The Death of Klinghoffer and the song cycle, The Wound Dresser; and numerous works of John Harbison. He was in the US premiere of The Lighthouse by Peter Maxwell Davies, the world premiere of Philip Glass' The Juniper Tree, and sang Sir Michael Tippett's The Ice Break at the BBC Proms. He portrays Klinghoffer in the film of John Adams’ The Death of Klinghoffer; receiving much acclaim and numerous international awards including a Grammy nomination. 2008 brought another Adams role for his debut with the Chicago Opera Theater: the Storyteller in John Adams’ The Flowering Tree conducted by the composer. He repeated this role at the Perth International Arts Festival in Australia (those performances won Australia’s 2009 Helpmann Award for Best Symphony Orchestra Performance) and again at Lincoln Center’s Mostly Mozart Festival. In 2011, to much acclaim, he sang the title role in Hindemith’s Cardillac with Opera Boston.</p>
